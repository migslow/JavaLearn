package ejercicios;

public class ej01 {

	public static void main(String[] args) {
		int num1 = 5;
		int num2 = 20;

		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 / num2);
		System.out.println(num1 * num2);
		System.out.println(num1 % num2);
		System.out.println(num2++);
		System.out.println(num2--);
		System.out.println(num1++);
		System.out.println(num1--);
	}

}
